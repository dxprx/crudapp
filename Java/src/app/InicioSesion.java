    public void entrar() throws SQLException {
        String usuario, password;
        usuario = jtf_usuario.getText();
        password = jtf_password.getText();
        Empleado user = conexion.iniciarSesion(usuario, password);
        if (user != null) {
            jtf_password.setBackground(Color.WHITE);
            user_pass_incorrecto.setVisible(false);
            JFrame main = new Aplicacion(user);
            this.dispose();
            main.setVisible(true);
        } else {
            jtf_password.setBackground(new Color(135, 32, 49, 150));
            user_pass_incorrecto.setVisible(true);
        }

    }

